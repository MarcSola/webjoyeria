function seccion1_over()	{
							onmouseover_blanco();
							document.getElementById("subseccion1").style.transition = "visibility 0.75s ease-in-out 0s,opacity 0.75s ease-in-out";
							document.getElementById("subseccion1").style.opacity = "1";
							document.getElementById("subseccion1").style.visibility = "visible";
							}


function seccion1_out()		{	
							onmouseout_negro();
							document.getElementById("subseccion1").style.transition = "visibility 0.75s ease-in-out 0s,opacity 0.75s ease-in-out";
							document.getElementById("subseccion1").style.opacity = "0";
							document.getElementById("subseccion1").style.visibility = "hidden";
							}

